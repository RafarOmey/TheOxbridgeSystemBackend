module.exports = (app) => {
    const users = require('../controllers/user.controller.js');

    // Retrieve all Users
    app.get('/users', users.findAll); 

    // Retrieve a single User with the given emailUsername
    app.get('/users/:userName', users.findOne); 

    // Update a User with the given emailUsername
    app.put('/users/:userName', users.update); 

    // Delete a User with the given emailUsername
    app.delete('/users/:userName', users.delete); 

    // Register a new admin
    app.post('/users/registerAdmin', users.registerAdmin); 

    // Register a new user
    app.post('/users/register', users.register); 

    // Login
    app.post('/users/login', users.login); 
}