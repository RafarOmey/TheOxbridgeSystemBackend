module.exports = (app) => {
    const ships = require('../controllers/ship.controller.js');

    // Create a new ship
    app.post('/ships', ships.create); 

    // Retrieve all ships
    app.get('/ships', ships.findAll); 

    // Retrieve a single ship
    app.get('/ships/:shipId', ships.findOne); 

    // Retrieve all ships participating in the given event
    app.get('/ships/fromEventId/:eventId', ships.findFromEventId); 

    //Retrieve all user ships
    app.get('/ships/myShips/fromUsername', ships.findMyShips); 

    // Update a ship
    app.put('/ships/:shipId', ships.update); 

    // Delete a ship
    app.delete('/ships/:shipId', ships.delete); 
}